export const getTitlesByDate = (ascending = false) => {
  return Object.entries(titleData).sort((a, b) => {
    const dateA = new Date(a[1].date);
    const dateB = new Date(b[1].date);
    return ascending ? dateA - dateB : dateB - dateA;
  });
};

