export const getYearStats = () => {
  const yearCounts = {};
  Object.values(titleData).forEach(item => {
    const year = new Date(item.date).getFullYear();
    yearCounts[year] = (yearCounts[year] || 0) + 1;
  });
  return yearCounts;
};
